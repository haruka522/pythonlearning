

# SyntaxError are the errors that are caused by code that doesn't follow the correct syntax of the programming language.
# SyntaxError can't be caught by except block
# Most IEDs(VScode or other code editors) will show the line number of the error(SyntaxError) or underline the line of the error

# Only LogicError (Exception) can be caught by except block
# IndexError, KeyError, NameError, TypeError, ValueError are the most common exceptions


